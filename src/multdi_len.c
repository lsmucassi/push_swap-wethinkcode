#include "../inc/checker.h"

int		multdi_len(char **str)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	
	while (i < ft_strlen(*str))
	{
		while (str[i][j])
		{
			j++;
		}
		i++;
	}
	return (j);
}
